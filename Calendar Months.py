# number of days in months

calculation_to_months = 31
name_of_unit = "Months"


def num_of_months():
    print(f"31 days are {31 / calculation_to_months} {name_of_unit}")
    print("Those are few Months")
    print(f"61 days are {61 / calculation_to_months} {name_of_unit}")
    print("Those are many Months")
    print(f"70 days are {70 / calculation_to_months} {name_of_unit}")
    print("Those are too Months")
    print(f"90 days are {90 / calculation_to_months} {name_of_unit}")
    print("Those are so many Months")
    print(f"210 days are {210 / calculation_to_months} {name_of_unit}")
    print("Those are too many Months")


num_of_months()
